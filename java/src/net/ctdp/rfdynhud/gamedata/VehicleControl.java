/**
 * 
 * @author Marvin Froehlich
 */
public enum VehicleControl
{
    NOBODY, // -1
    LOCAL_PLAYER, // 0
    LOCAL_AI, // 1
    REMOTE, // 2
    REPLAY, // 3
    ;
    
    public final boolean isLocalPlayer()
    {
        return ( this == LOCAL_PLAYER );
    }
}
